"""
W1-2 consolidation template — Lina's Coffee.

Copied into the workshop folder at Beat 8. Adapt COLUMNS and category
keywords if the learner picked anything non-default; otherwise run as-is.

Output: data/consolidated.csv

Reads 6 source files in data-dump/ and merges them into one clean sheet.
Revenue is the sum of `type=revenue` rows (POS detail). There is no
`revenue_summary` type — POS is the single source of truth.

Generated source files (POS in Loyverse Sales-by-Receipt shape, bank in
Bangkok Bank iBanking shape) carry a `# Generated by ...` header line.
This script skips that comment header before reading the CSV body.
"""

import csv
import os
import re
import sys

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8")

BASE = os.path.dirname(os.path.abspath(__file__))
DATA_DUMP = os.path.join(BASE, "data-dump")
OUT_DIR = os.path.join(BASE, "data")
OUT_FILE = os.path.join(OUT_DIR, "consolidated.csv")

FX = 32  # 1 USD = 32 THB (locked constant)

COLUMNS = [
    "date", "type", "category", "vendor_or_party",
    "description", "amount_thb", "amount_usd",
    "currency_original", "source_file", "notes"
]

CATEGORIES = ["Sales", "Coffee Beans", "Dairy & Pastry", "Payroll",
              "Rent & Utilities", "Other"]


def thb_to_usd(thb):
    return round(float(thb) / FX, 2)


def usd_to_thb(usd):
    return round(float(usd) * FX, 2)


def categorize(text):
    t = text.lower()
    if any(w in t for w in ["equipment service", "cleaning", "marketing",
                             "ig ads", "meta ads", "flyer", "decor",
                             "atm withdraw", "transfer", "savings"]):
        return "Other"
    if any(w in t for w in ["bean", "ethiopian", "highland"]):
        return "Coffee Beans"
    if any(w in t for w in ["milk", "dairy", "pastry", "greenleaf",
                             "bangkok dairy"]):
        return "Dairy & Pastry"
    if any(w in t for w in ["salary", "payroll", "bonus", "wage"]):
        return "Payroll"
    if any(w in t for w in ["rent", "electric", "water", "internet",
                             "utility", "utilities", "property tax",
                             "insurance", "fastnet", "nk property",
                             "city power", "city water"]):
        return "Rent & Utilities"
    return "Other"


def open_csv(path):
    """Open a generated CSV, skipping the leading `#`-comment header if present."""
    f = open(path, encoding="utf-8")
    pos = f.tell()
    first = f.readline()
    if not first.lstrip().startswith("#"):
        f.seek(pos)
    return f


rows = []


# ── 1. pos_export_oct2025-mar2026.csv (Loyverse Sales-by-Receipt shape) ────
pos_count = 0
with open_csv(os.path.join(DATA_DUMP, "pos_export_oct2025-mar2026.csv")) as f:
    for r in csv.DictReader(f):
        ts = r["Receipt date"]
        d = ts[:10] if ts else ""
        net = float(r.get("Net sales") or 0)
        receipt_type = (r.get("Receipt type") or "Sale").strip()
        is_void = receipt_type.lower() == "void"
        description = f"{r['Item name']} {r.get('Variant', '')}".strip()
        description = f"{description}: terminal {r['terminal_id']} staff {r['staff_id']}"
        rows.append({
            "date": d,
            "type": "refund" if is_void else "revenue",
            "category": "Sales",
            "vendor_or_party": "Customer",
            "description": description,
            "amount_thb": net,
            "amount_usd": thb_to_usd(net),
            "currency_original": "THB",
            "source_file": "pos_export_oct2025-mar2026.csv",
            "notes": "void" if is_void else "",
        })
        pos_count += 1
print(f"pos_export             : {pos_count} rows")


# ── 2. expenses_2025.csv ───────────────────────────────────────────────────
exp_count = 0
with open_csv(os.path.join(DATA_DUMP, "expenses_2025.csv")) as f:
    for r in csv.DictReader(f):
        if not r.get("date"):
            continue
        cur = (r.get("currency") or "THB").strip().upper()
        amt_raw = float(r.get("amount", 0) or 0)
        if cur == "USD":
            thb = usd_to_thb(amt_raw)
            usd = amt_raw
        else:
            thb = float(r.get("amount_thb", amt_raw) or amt_raw)
            usd = thb_to_usd(thb)
        cat_raw = (r.get("category") or "").strip()
        vendor = (r.get("vendor") or "").strip()
        cat = categorize(cat_raw or vendor) if not cat_raw else (
            cat_raw if cat_raw.lower() in {c.lower() for c in CATEGORIES}
            else categorize(vendor)
        )
        # Normalize category-label case
        for canonical in CATEGORIES:
            if cat.lower() == canonical.lower():
                cat = canonical
                break
        rows.append({
            "date": r["date"].strip(),
            "type": "expense",
            "category": cat,
            "vendor_or_party": vendor,
            "description": vendor,
            "amount_thb": -thb,
            "amount_usd": -usd,
            "currency_original": cur,
            "source_file": "expenses_2025.csv",
            "notes": (r.get("notes") or "").strip(),
        })
        exp_count += 1
print(f"expenses_2025.csv      : {exp_count} rows")


# ── 3. bank_statement_abc.csv (Bangkok Bank iBanking shape) ────────────────
bank_count = 0
with open_csv(os.path.join(DATA_DUMP, "bank_statement_abc.csv")) as f:
    for r in csv.DictReader(f):
        if not r.get("Date"):
            continue
        withdrawal = float(r.get("Withdrawal", 0) or 0)
        deposit = float(r.get("Deposit", 0) or 0)
        desc = (r.get("Description") or "").strip()
        thb = -withdrawal if withdrawal > 0 else deposit
        rows.append({
            "date": r["Date"].strip(),
            "type": "transfer",
            "category": categorize(desc),
            "vendor_or_party": desc,
            "description": desc,
            "amount_thb": thb,
            "amount_usd": thb_to_usd(thb),
            "currency_original": "THB",
            "source_file": "bank_statement_abc.csv",
            "notes": (r.get("Channel") or "").strip(),
        })
        bank_count += 1
print(f"bank_statement_abc.csv : {bank_count} rows")


# ── 4. supplier_invoices.csv ───────────────────────────────────────────────
sup_count = 0
with open_csv(os.path.join(DATA_DUMP, "supplier_invoices.csv")) as f:
    for r in csv.DictReader(f):
        if not r.get("date"):
            continue
        thb = float(r.get("total_thb", 0) or 0)
        vendor = r.get("vendor", "").strip()
        item = r.get("item", "").strip()
        rows.append({
            "date": r["date"].strip(),
            "type": "expense",
            "category": categorize(vendor + " " + item),
            "vendor_or_party": vendor,
            "description": item,
            "amount_thb": -thb,
            "amount_usd": -thb_to_usd(thb),
            "currency_original": "THB",
            "source_file": "supplier_invoices.csv",
            "notes": f"inv {r.get('invoice_no', '')} paid={r.get('paid', '')}",
        })
        sup_count += 1
print(f"supplier_invoices.csv  : {sup_count} rows")


# ── 5. staff_payroll.csv ───────────────────────────────────────────────────
pay_count = 0
with open_csv(os.path.join(DATA_DUMP, "staff_payroll.csv")) as f:
    for r in csv.DictReader(f):
        month = r.get("month", "").strip()
        if not month:
            continue
        d = month + "-25"
        total = float(r.get("total_paid_thb", 0) or 0)
        cash_note = r.get("cash_note", "").strip()
        rows.append({
            "date": d,
            "type": "expense",
            "category": "Payroll",
            "vendor_or_party": r.get("name", "").strip(),
            "description": f"Salary {r.get('name', '').strip()} {month}",
            "amount_thb": -total,
            "amount_usd": -thb_to_usd(total),
            "currency_original": "THB",
            "source_file": "staff_payroll.csv",
            "notes": "cash extra" if cash_note else "",
        })
        pay_count += 1
print(f"staff_payroll.csv      : {pay_count} rows")


# ── 6. rent_utilities.txt ──────────────────────────────────────────────────
MONTHS = ["2025-10", "2025-11", "2025-12", "2026-01", "2026-02", "2026-03"]
ELEC = {"2025-10": 7200, "2025-11": 6800, "2025-12": 8400,
        "2026-01": 7900, "2026-02": 7600, "2026-03": 9100}
WATER = {"2025-10": 800, "2025-11": 750, "2025-12": 820,
         "2026-01": 780, "2026-02": 810, "2026-03": 790}
rent_util_entries = [
    ("Rent", "NK Property", 45000),
    ("Internet", "FastNet", 1290),
]
ru_count = 0
for mo in MONTHS:
    d = mo + "-05"
    for desc, vendor, thb in rent_util_entries:
        rows.append({
            "date": d, "type": "expense", "category": "Rent & Utilities",
            "vendor_or_party": vendor,
            "description": desc,
            "amount_thb": -thb, "amount_usd": -thb_to_usd(thb),
            "currency_original": "THB",
            "source_file": "rent_utilities.txt", "notes": ""
        })
        ru_count += 1
    # Electric (varies)
    elec = ELEC.get(mo, 0)
    rows.append({
        "date": mo + "-22", "type": "expense", "category": "Rent & Utilities",
        "vendor_or_party": "City Power Electric", "description": "Electric",
        "amount_thb": -elec, "amount_usd": -thb_to_usd(elec),
        "currency_original": "THB", "source_file": "rent_utilities.txt",
        "notes": ""
    })
    # Water
    water = WATER.get(mo, 0)
    rows.append({
        "date": mo + "-15", "type": "expense", "category": "Rent & Utilities",
        "vendor_or_party": "City Water Util", "description": "Water",
        "amount_thb": -water, "amount_usd": -thb_to_usd(water),
        "currency_original": "THB", "source_file": "rent_utilities.txt",
        "notes": ""
    })
    ru_count += 2
rows.append({
    "date": "2025-11-12", "type": "expense", "category": "Rent & Utilities",
    "vendor_or_party": "Revenue Dept", "description": "Property tax",
    "amount_thb": -18000, "amount_usd": -thb_to_usd(18000),
    "currency_original": "THB", "source_file": "rent_utilities.txt",
    "notes": "annual"
})
rows.append({
    "date": "2025-10-18", "type": "expense", "category": "Rent & Utilities",
    "vendor_or_party": "Insurer", "description": "Insurance",
    "amount_thb": -24000, "amount_usd": -thb_to_usd(24000),
    "currency_original": "THB", "source_file": "rent_utilities.txt",
    "notes": "annual"
})
ru_count += 2
print(f"rent_utilities.txt     : {ru_count} rows")


# ── Write output ──────────────────────────────────────────────────────────
os.makedirs(OUT_DIR, exist_ok=True)
rows.sort(key=lambda r: r["date"])

with open(OUT_FILE, "w", newline="", encoding="utf-8") as f:
    writer = csv.DictWriter(f, fieldnames=COLUMNS)
    writer.writeheader()
    writer.writerows(rows)

total = len(rows)
revenue = sum(r["amount_thb"] for r in rows if r["type"] == "revenue")
expenses = sum(r["amount_thb"] for r in rows if r["type"] == "expense")
net = revenue + expenses

print(f"\n{'-'*45}")
print(f"Total rows merged      : {total:,}")
print(f"Revenue (POS)          : THB {revenue:,.0f}")
print(f"Expenses (THB)         : THB {expenses:,.0f}")
print(f"Net (THB)              : THB {net:,.0f}  (approx. ${net/FX:,.0f} USD)")
print(f"Output                 : data/consolidated.csv")
print(f"{'-'*45}")
